    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int k = Integer.parseInt(JOptionPane.showInputDialog("Ingresa un número: "));
        int m = Integer.parseInt(JOptionPane.showInputDialog("Ingresa un número: "));
        if (!colaNumeros.estaVacia()) {
            colaNumeros.invetirConRango(k, m);
        }
        jTextArea2.setText(colaNumeros.toString());
    }//GEN-LAST:event_jButton7ActionPerformed


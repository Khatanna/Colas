    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String dato = JOptionPane.showInputDialog("Ingresa un número: ");

        if (dato.contains(",")) {
            String[] array = dato.split(",");
            colaNumeros.forEach(array);
        } else {
            colaNumeros.encolar(Integer.parseInt(dato));
        }
        jTextArea1.setText(colaNumeros.toString());
    }//GEN-LAST:event_jButton3ActionPerformed


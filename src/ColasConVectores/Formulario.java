    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int limite = Integer.parseInt(JOptionPane.showInputDialog("Cuantos quieres mi rey üëåüòÅ"));
        
        for (int i = 0; i < limite; i++) {
            char letra = (char) (Math.random() * 10 + 'a');
            cola.encolar((letra+"").toString().charAt(0));
        }
        jTextArea1.setText(cola.toString());
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String palabra = JOptionPane.showInputDialog("Ingresa una palabra");
        palabra = palabra.replace(" ", "");
             
        for (int i = 0; i < palabra.length(); i++) {
            cola.encolar(palabra.charAt(i));
        }
        jTextArea1.setText(cola.toString());
    }//GEN-LAST:event_jButton5ActionPerformed

